# 9. Collate
# =========================================================
def collate_fn(features):
    batch = {
        "texts": [f["text"] for f in features],
        "input_ids": torch.stack([f["input_ids"] for f in features], dim=0),
        "attention_mask": torch.stack([f["attention_mask"] for f in features], dim=0),
        "labels": torch.stack([f["labels"] for f in features], dim=0),
        "entities": [f["entities"] for f in features],
        "splits": [f["split"] for f in features],
    }

    token_type_ids_list = [f["token_type_ids"] for f in features]
    if all(x is not None for x in token_type_ids_list):
        batch["token_type_ids"] = torch.stack(token_type_ids_list, dim=0)
    else:
        batch["token_type_ids"] = None

    return batch


