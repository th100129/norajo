# 5. RoPE 캐시
# =========================================================
class RoPEPositionEncoding(nn.Module):
    def __init__(self, max_seq_len: int, inner_dim: int):
        super().__init__()
        self.max_seq_len = max_seq_len
        self.inner_dim = inner_dim

        position_ids = torch.arange(0, max_seq_len, dtype=torch.float).unsqueeze(-1)
        indices = torch.arange(0, inner_dim // 2, dtype=torch.float)
        indices = torch.pow(10000, -2 * indices / inner_dim)

        embeddings = position_ids * indices
        sin_embeddings = torch.sin(embeddings)
        cos_embeddings = torch.cos(embeddings)

        pos_emb = torch.stack([sin_embeddings, cos_embeddings], dim=-1).reshape(max_seq_len, inner_dim)
        self.register_buffer("pos_emb", pos_emb, persistent=False)

    def forward(self, seq_len: int, batch_size: int) -> torch.Tensor:
        return self.pos_emb[:seq_len].unsqueeze(0).expand(batch_size, -1, -1)


def apply_rope(x: torch.Tensor, pos_emb: torch.Tensor) -> torch.Tensor:
    """
    x: (batch, seq_len, ent_type_size, inner_dim)
    pos_emb: (batch, seq_len, inner_dim)
    """
    cos_pos = pos_emb[..., 1::2].repeat_interleave(2, dim=-1)
    sin_pos = pos_emb[..., ::2].repeat_interleave(2, dim=-1)

    x2 = torch.stack([-x[..., 1::2], x[..., ::2]], dim=-1).reshape_as(x)
    return x * cos_pos.unsqueeze(2) + x2 * sin_pos.unsqueeze(2)


