# 8. Dataset
# =========================================================
class LocalJsonlNERDataset(Dataset):
    def __init__(
        self,
        data: List[Dict],
        label2id: Dict[str, int],
        split_name: str
    ):
        self.data = data
        self.label2id = label2id
        self.ent_type_size = len(label2id)
        self.split_name = split_name

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        sample = self.data[idx]

        text = sample["text"]
        input_ids = sample["input_ids"]
        attention_mask = sample["attention_mask"]
        token_type_ids = sample.get("token_type_ids", None)
        entities = sample.get("entities", [])

        seq_len = len(input_ids)

        if len(attention_mask) != seq_len:
            raise ValueError(
                f"[{self.split_name}] attention_mask 길이가 input_ids와 다릅니다. "
                f"idx={idx}, len(input_ids)={seq_len}, len(attention_mask)={len(attention_mask)}"
            )

        if token_type_ids is not None and len(token_type_ids) != seq_len:
            raise ValueError(
                f"[{self.split_name}] token_type_ids 길이가 input_ids와 다릅니다. "
                f"idx={idx}, len(input_ids)={seq_len}, len(token_type_ids)={len(token_type_ids)}"
            )

        labels = torch.zeros((self.ent_type_size, seq_len, seq_len), dtype=torch.float32)

        for ent in entities:
            start = ent["start"]
            end = ent["end"]
            label = ent["label"]

            if label not in self.label2id:
                continue
            if not (0 <= start < seq_len and 0 <= end < seq_len):
                continue
            if end < start:
                continue

            labels[self.label2id[label], start, end] = 1.0

        item = {
            "text": text,
            "input_ids": torch.tensor(input_ids, dtype=torch.long),
            "attention_mask": torch.tensor(attention_mask, dtype=torch.long),
            "labels": labels,
            "entities": entities,
            "split": self.split_name,
        }

        if token_type_ids is not None:
            item["token_type_ids"] = torch.tensor(token_type_ids, dtype=torch.long)
        else:
            item["token_type_ids"] = None

        return item


