# 17. Predict
# =========================================================
@torch.no_grad()
def predict_from_sample(model, cfg, id2label, sample: Dict, threshold: float = 0.0):
    model.eval()

    input_ids = torch.tensor(sample["input_ids"], dtype=torch.long).unsqueeze(0).to(cfg.device)
    attention_mask = torch.tensor(sample["attention_mask"], dtype=torch.long).unsqueeze(0).to(cfg.device)

    token_type_ids = sample.get("token_type_ids", None)
    if token_type_ids is not None:
        token_type_ids = torch.tensor(token_type_ids, dtype=torch.long).unsqueeze(0).to(cfg.device)

    amp_dtype = torch.float16 if cfg.device.startswith("cuda") else torch.float32

    with torch.autocast(
        device_type="cuda",
        dtype=amp_dtype,
        enabled=(cfg.use_amp and cfg.device.startswith("cuda"))
    ):
        outputs = model(
            input_ids=input_ids,
            attention_mask=attention_mask,
            token_type_ids=token_type_ids
        )
        logits = outputs["logits"].squeeze(0).float().cpu()

    pred_entities = decode_entities_from_logits_fast(logits, id2label, threshold=threshold)
    return pred_entities


