@torch.no_grad()
def compute_f1_fast(model, dataloader, device, id2label, threshold=0.0, use_amp=True):
    model.eval()
    tp, fp, fn = 0, 0, 0

    amp_dtype = torch.float16 if device.startswith("cuda") else torch.float32

    for batch in dataloader:
        input_ids = batch["input_ids"].to(device, non_blocking=True)
        attention_mask = batch["attention_mask"].to(device, non_blocking=True)
        labels = batch["labels"].to(device, non_blocking=True)

        token_type_ids = batch["token_type_ids"]
        if token_type_ids is not None:
            token_type_ids = token_type_ids.to(device, non_blocking=True)

        with torch.autocast(device_type="cuda", dtype=amp_dtype, enabled=(use_amp and device.startswith("cuda"))):
            outputs = model(
                input_ids=input_ids,
                attention_mask=attention_mask,
                token_type_ids=token_type_ids
            )
            logits = outputs["logits"]

        logits = logits.float().cpu()
        labels = labels.cpu()

        for b in range(input_ids.size(0)):
            gold = extract_gold_entities_fast(labels[b], id2label)
            pred = extract_pred_entities_fast(logits[b], id2label, threshold)

            tp += len(gold & pred)
            fp += len(pred - gold)
            fn += len(gold - pred)

    precision = tp / (tp + fp + 1e-12)
    recall = tp / (tp + fn + 1e-12)
    f1 = 2 * precision * recall / (precision + recall + 1e-12)
    return precision, recall, f1


# =========================================================
# 12. Decode
# =========================================================
def decode_entities_from_logits_fast(logits: torch.Tensor, id2label: Dict[int, str], threshold: float = 0.0):
    """
    logits: (C, L, L)
    """
    indices = torch.nonzero(logits > threshold, as_tuple=False)
    if indices.numel() == 0:
        return []

    scores = logits[indices[:, 0], indices[:, 1], indices[:, 2]]

    entities = []
    for idx, score in zip(indices, scores):
        c, s, e = idx.tolist()
        entities.append({
            "label": id2label[c],
            "start": s,
            "end": e,
            "score": round(float(score.item()), 4)
        })

    entities.sort(key=lambda x: x["score"], reverse=True)
    return entities


# =========================================================
# 13. BIO 변환
# =========================================================
def convert_entities_to_bio(seq_len: int, entities: List[Dict]) -> List[str]:
    """
    span entity -> BIO tag
    entities: [{"label": "PER", "start": 1, "end": 3, "score": 0.95}, ...]
    """
    bio_tags = ["O"] * seq_len

    # score 높은 span부터 우선 반영
    sorted_entities = sorted(entities, key=lambda x: x.get("score", 0.0), reverse=True)
    occupied = [False] * seq_len

    for ent in sorted_entities:
        start = ent["start"]
        end = ent["end"]
        label = ent["label"]

        if not (0 <= start < seq_len and 0 <= end < seq_len):
            continue
        if end < start:
            continue

        # 이미 다른 엔티티가 차지한 구간이면 skip
        if any(occupied[i] for i in range(start, end + 1)):
            continue

        bio_tags[start] = f"B-{label}"
        occupied[start] = True

        for i in range(start + 1, end + 1):
            bio_tags[i] = f"I-{label}"
            occupied[i] = True

    return bio_tags


def convert_gold_entities_to_bio(seq_len: int, gold_entities: List[Dict]) -> List[str]:
    """
    gold entity -> BIO tag
    gold entity는 score가 없을 수 있으므로 별도 처리
    """
    bio_tags = ["O"] * seq_len

    for ent in gold_entities:
        start = ent["start"]
        end = ent["end"]
        label = ent["label"]

        if not (0 <= start < seq_len and 0 <= end < seq_len):
            continue
        if end < start:
            continue

        bio_tags[start] = f"B-{label}"
        for i in range(start + 1, end + 1):
            bio_tags[i] = f"I-{label}"

    return bio_tags


# =========================================================
# 14. JSON 출력 구성
# =========================================================
def build_prediction_output(sample: Dict, pred_entities: List[Dict]) -> Dict:
    seq_len = len(sample["input_ids"])

    pred_bio_tags = convert_entities_to_bio(seq_len, pred_entities)
    gold_bio_tags = convert_gold_entities_to_bio(seq_len, sample.get("entities", []))

    return {
        "text": sample["text"],
        "tokens": sample["input_ids"],  # 토큰 단위 동일하게 유지
        "gold_entities": sample.get("entities", []),
        "gold_bio_tags": gold_bio_tags,
        "raw_predictions": pred_entities,
        "pred_bio_tags": pred_bio_tags,
        "confidence_scores": [
            {
                "label": ent["label"],
                "start": ent["start"],
                "end": ent["end"],
                "score": ent["score"]
            }
            for ent in pred_entities
        ]
    }


