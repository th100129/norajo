# 6. GlobalPointer Head
# =========================================================
class GlobalPointer(nn.Module):
    def __init__(self, hidden_size: int, ent_type_size: int, inner_dim: int, max_seq_len: int = 512, use_rope: bool = True):
        super().__init__()
        self.ent_type_size = ent_type_size
        self.inner_dim = inner_dim
        self.use_rope = use_rope
        self.dense = nn.Linear(hidden_size, ent_type_size * inner_dim * 2)

        if use_rope:
            self.rope = RoPEPositionEncoding(max_seq_len=max_seq_len, inner_dim=inner_dim)

    def forward(self, last_hidden_state: torch.Tensor, attention_mask: torch.Tensor) -> torch.Tensor:
        batch_size, seq_len, _ = last_hidden_state.size()

        outputs = self.dense(last_hidden_state)
        outputs = outputs.view(batch_size, seq_len, self.ent_type_size, self.inner_dim * 2)
        qw, kw = outputs[..., :self.inner_dim], outputs[..., self.inner_dim:]

        if self.use_rope:
            pos_emb = self.rope(seq_len=seq_len, batch_size=batch_size)
            qw = apply_rope(qw, pos_emb)
            kw = apply_rope(kw, pos_emb)

        logits = torch.einsum("bmhd,bnhd->bhmn", qw, kw)
        logits = logits / math.sqrt(self.inner_dim)

        mask1 = attention_mask.unsqueeze(1).unsqueeze(3)
        mask2 = attention_mask.unsqueeze(1).unsqueeze(2)
        pad_mask = mask1 * mask2
        logits = logits * pad_mask - (1 - pad_mask) * 1e12

        tril_mask = torch.tril(
            torch.ones(seq_len, seq_len, device=logits.device, dtype=logits.dtype),
            diagonal=-1
        )
        logits = logits - tril_mask.unsqueeze(0).unsqueeze(0) * 1e12

        return logits


