# 3. label map 생성
# =========================================================
def build_label_maps(train_data: List[Dict], valid_data: List[Dict]) -> Tuple[Dict[str, int], Dict[int, str]]:
    entity_types = set()

    for dataset in [train_data, valid_data]:
        for sample in dataset:
            for ent in sample.get("entities", []):
                entity_types.add(ent["label"])

    entity_types = sorted(entity_types)
    label2id = {label: idx for idx, label in enumerate(entity_types)}
    id2label = {idx: label for label, idx in label2id.items()}
    return label2id, id2label


