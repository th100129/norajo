def build_dataloaders(cfg: Config):
    train_data = load_jsonl_file(cfg.train_json_path)
    valid_data = load_jsonl_file(cfg.valid_json_path)

    print(f"[INFO] train samples: {len(train_data)}")
    print(f"[INFO] valid samples: {len(valid_data)}")

    label2id, id2label = build_label_maps(train_data, valid_data)
    print(f"[INFO] label2id: {label2id}")

    train_dataset = LocalJsonlNERDataset(
        data=train_data,
        label2id=label2id,
        split_name="train"
    )
    valid_dataset = LocalJsonlNERDataset(
        data=valid_data,
        label2id=label2id,
        split_name="valid"
    )

    common_loader_kwargs = {
        "batch_size": cfg.batch_size,
        "collate_fn": collate_fn,
        "num_workers": cfg.num_workers,
        "pin_memory": torch.cuda.is_available(),
        "persistent_workers": cfg.num_workers > 0,
    }

    train_loader = DataLoader(
        train_dataset,
        shuffle=True,
        **common_loader_kwargs
    )
    valid_loader = DataLoader(
        valid_dataset,
        shuffle=False,
        **common_loader_kwargs
    )

    return label2id, id2label, train_loader, valid_loader


