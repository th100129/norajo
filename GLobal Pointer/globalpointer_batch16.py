# 2. JSON / JSONL 로드
#    Args:
#        json_path (str): json 또는 jsonl 파일 경로
#
#    Returns:
#        data: List[Dict]
#    
#    Raises:
#        FileNotFoundError: 파일을 찾을 수 없습니다.
#        ValueError: 파일이 비어 있습니다.
#                    JSON은 리스트 형태여야 합니다.
#        JSONDecodeError: JSON 파싱 실패
# =========================================================
def load_jsonl_file(json_path: str) -> List[Dict]:
    if not os.path.exists(json_path):
        raise FileNotFoundError(f"파일을 찾을 수 없습니다: {json_path}")

    with open(json_path, "r", encoding="utf-8") as f:
        content = f.read().strip()

    if not content:
        raise ValueError(f"파일이 비어 있습니다: {json_path}")

    if content.startswith("["):
        try:
            data = json.loads(content)
            if not isinstance(data, list):
                raise ValueError("JSON은 리스트 형태여야 합니다.")
            return data
        except json.JSONDecodeError as e:
            raise ValueError(f"JSON 파싱 실패: {e}")

    data = []
    for line_num, line in enumerate(content.splitlines(), start=1):
        line = line.strip()
        if not line:
            continue
        try:
            data.append(json.loads(line))
        except json.JSONDecodeError as e:
            raise ValueError(f"{json_path} 파일의 {line_num}번째 줄 파싱 실패: {e}")

    return data


