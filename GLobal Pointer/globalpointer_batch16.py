# 3. label map 생성
#    Args:
#        train_data (List[Dict]): train 데이터
#        valid_data (List[Dict]): valid 데이터
#
#    Returns:
#        label2id (Dict[str, int]): 라벨명을 정수 id로 매핑한 딕셔너리
#        id2label (Dict[int, str]): 정수 id를 라벨명으로 매핑한 딕셔너리
# =========================================================
def build_label_maps(train_data: List[Dict], valid_data: List[Dict]) -> Tuple[Dict[str, int], Dict[int, str]]:
    entity_types = set()

    for dataset in [train_data, valid_data]:
        for sample in dataset:
            for ent in sample.get("entities", []):
                entity_types.add(ent["label"])

    entity_types = sorted(entity_types)
    label2id = {label: idx for idx, label in enumerate(entity_types)}
    id2label = {idx: label for label, idx in label2id.items()}
    return label2id, id2label


# =========================================================
# 4. Multi-label Categorical Crossentropy Loss
#    Args:
#        y_pred (torch.Tensor): 예측 logits, shape = (B, C, L, L)
#        y_true (torch.Tensor): 정답 label tensor, shape = (B, C, L, L)
#
#    Returns:
#        loss (torch.Tensor): 평균 loss
# =========================================================
def multilabel_categorical_crossentropy(y_pred: torch.Tensor, y_true: torch.Tensor) -> torch.Tensor:
    """
    y_pred, y_true: (batch, ent_type_size, seq_len, seq_len)
    """
    y_pred = (1 - 2 * y_true) * y_pred
    y_pred_neg = y_pred - y_true * 1e12
    y_pred_pos = y_pred - (1 - y_true) * 1e12

    zeros = torch.zeros_like(y_pred[..., :1])
    y_pred_neg = torch.cat([y_pred_neg, zeros], dim=-1)
    y_pred_pos = torch.cat([y_pred_pos, zeros], dim=-1)

    neg_loss = torch.logsumexp(y_pred_neg, dim=-1)
    pos_loss = torch.logsumexp(y_pred_pos, dim=-1)

    return (neg_loss + pos_loss).mean()


# =========================================================
# 5. RoPE positional embedding 캐시 클래스
#    Args:
#        max_seq_len (int): 최대 시퀀스 길이
#        inner_dim (int): RoPE 적용 차원
#
#    Attributes:
#        pos_emb (torch.Tensor): 미리 계산해둔 positional embedding
# =========================================================
class RoPEPositionEncoding(nn.Module):
    def __init__(self, max_seq_len: int, inner_dim: int):
        super().__init__()
        self.max_seq_len = max_seq_len
        self.inner_dim = inner_dim

        position_ids = torch.arange(0, max_seq_len, dtype=torch.float).unsqueeze(-1)
        indices = torch.arange(0, inner_dim // 2, dtype=torch.float)
        indices = torch.pow(10000, -2 * indices / inner_dim)

        embeddings = position_ids * indices
        sin_embeddings = torch.sin(embeddings)
        cos_embeddings = torch.cos(embeddings)

        pos_emb = torch.stack([sin_embeddings, cos_embeddings], dim=-1).reshape(max_seq_len, inner_dim)
        self.register_buffer("pos_emb", pos_emb, persistent=False)

    # =========================================================
    # 5-1. 캐시된 positional embedding 반환
    #    Args:
    #        seq_len (int): 현재 입력 시퀀스 길이
    #        batch_size (int): 현재 배치 크기
    #
    #    Returns:
    #        pos_emb (torch.Tensor): shape = (B, L, D)
    # =========================================================
    def forward(self, seq_len: int, batch_size: int) -> torch.Tensor:
        return self.pos_emb[:seq_len].unsqueeze(0).expand(batch_size, -1, -1)

# =========================================================
# 6. RoPE 적용 함수
#    Args:
#        x (torch.Tensor): 입력 텐서, shape = (B, L, C, D)
#        pos_emb (torch.Tensor): positional embedding, shape = (B, L, D)
#
#    Returns:
#        rotated_x (torch.Tensor): RoPE가 적용된 텐서
# =========================================================
def apply_rope(x: torch.Tensor, pos_emb: torch.Tensor) -> torch.Tensor:
    """
    x: (batch, seq_len, ent_type_size, inner_dim)
    pos_emb: (batch, seq_len, inner_dim)
    """
    cos_pos = pos_emb[..., 1::2].repeat_interleave(2, dim=-1)
    sin_pos = pos_emb[..., ::2].repeat_interleave(2, dim=-1)

    x2 = torch.stack([-x[..., 1::2], x[..., ::2]], dim=-1).reshape_as(x)
    return x * cos_pos.unsqueeze(2) + x2 * sin_pos.unsqueeze(2)


# =========================================================
# 7. GlobalPointer Head
#    Args:
#        hidden_size (int): encoder hidden size
#        ent_type_size (int): 엔티티 타입 개수
#        inner_dim (int): GlobalPointer 내부 차원
#        max_seq_len (int): 최대 시퀀스 길이
#        use_rope (bool): RoPE 사용 여부
# =========================================================
class GlobalPointer(nn.Module):
    def __init__(self, hidden_size: int, ent_type_size: int, inner_dim: int, max_seq_len: int = 512, use_rope: bool = True):
        super().__init__()
        self.ent_type_size = ent_type_size
        self.inner_dim = inner_dim
        self.use_rope = use_rope
        self.dense = nn.Linear(hidden_size, ent_type_size * inner_dim * 2)

        if use_rope:
            self.rope = RoPEPositionEncoding(max_seq_len=max_seq_len, inner_dim=inner_dim)

    # =========================================================
    # 7-1. GlobalPointer forward
    #    Args:
    #        last_hidden_state (torch.Tensor): encoder 출력, shape = (B, L, H)
    #        attention_mask (torch.Tensor): attention mask, shape = (B, L)
    #
    #    Returns:
    #        logits (torch.Tensor): span score, shape = (B, C, L, L)
    # =========================================================
    def forward(self, last_hidden_state: torch.Tensor, attention_mask: torch.Tensor) -> torch.Tensor:
        batch_size, seq_len, _ = last_hidden_state.size()

        outputs = self.dense(last_hidden_state)
        outputs = outputs.view(batch_size, seq_len, self.ent_type_size, self.inner_dim * 2)
        qw, kw = outputs[..., :self.inner_dim], outputs[..., self.inner_dim:]

        if self.use_rope:
            pos_emb = self.rope(seq_len=seq_len, batch_size=batch_size)
            qw = apply_rope(qw, pos_emb)
            kw = apply_rope(kw, pos_emb)

        logits = torch.einsum("bmhd,bnhd->bhmn", qw, kw)
        logits = logits / math.sqrt(self.inner_dim)

        mask1 = attention_mask.unsqueeze(1).unsqueeze(3)
        mask2 = attention_mask.unsqueeze(1).unsqueeze(2)
        pad_mask = mask1 * mask2
        logits = logits * pad_mask - (1 - pad_mask) * 1e12

        tril_mask = torch.tril(
            torch.ones(seq_len, seq_len, device=logits.device, dtype=logits.dtype),
            diagonal=-1
        )
        logits = logits - tril_mask.unsqueeze(0).unsqueeze(0) * 1e12

        return logits


# =========================================================
# 8. Encoder(Bert) + GlobalPointer 모델
#    Args:
#        pretrained_model_name (str): 사용할 사전학습 모델 이름
#        ent_type_size (int): 엔티티 타입 개수
#        inner_dim (int): GlobalPointer 내부 차원
#        max_seq_len (int): 최대 시퀀스 길이
# =========================================================
class BertGlobalPointerForNER(nn.Module):
    def __init__(self, pretrained_model_name: str, ent_type_size: int, inner_dim: int, max_seq_len: int = 512):
        super().__init__()
        self.encoder = AutoModel.from_pretrained(pretrained_model_name)
        hidden_size = self.encoder.config.hidden_size

        self.global_pointer = GlobalPointer(
            hidden_size=hidden_size,
            ent_type_size=ent_type_size,
            inner_dim=inner_dim,
            max_seq_len=max_seq_len,
            use_rope=True
        )

    # =========================================================
    # 8-1. 모델 forward
    #    Args:
    #        input_ids (torch.Tensor): 입력 토큰 id
    #        attention_mask (torch.Tensor): attention mask
    #        token_type_ids (torch.Tensor | None): token type ids
    #        labels (torch.Tensor | None): 정답 label tensor
    #
    #    Returns:
    #        result (Dict): logits, labels가 있으면 loss 포함
    # =========================================================
    def forward(self, input_ids, attention_mask, token_type_ids=None, labels=None):
        encoder_kwargs = {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
        }

        if token_type_ids is not None:
            try:
                outputs = self.encoder(
                    input_ids=input_ids,
                    attention_mask=attention_mask,
                    token_type_ids=token_type_ids
                )
            except TypeError:
                outputs = self.encoder(**encoder_kwargs)
        else:
            outputs = self.encoder(**encoder_kwargs)

        last_hidden_state = outputs.last_hidden_state
        logits = self.global_pointer(last_hidden_state, attention_mask)

        result = {"logits": logits}
        if labels is not None:
            result["loss"] = multilabel_categorical_crossentropy(logits, labels)
        return result


# =========================================================
# 9. Local JSON/JSONL NER Dataset
#    Args:
#        data (List[Dict]): 데이터 샘플 리스트
#        label2id (Dict[str, int]): 라벨-아이디 매핑
#        split_name (str): 데이터 구분(train/valid/test)
# =========================================================
class LocalJsonlNERDataset(Dataset):
    def __init__(
        self,
        data: List[Dict],
        label2id: Dict[str, int],
        split_name: str
    ):
        self.data = data
        self.label2id = label2id
        self.ent_type_size = len(label2id)
        self.split_name = split_name

    # =========================================================
    # 9-1. 데이터셋 길이 반환
    #    Returns:
    #        length (int): 데이터셋 샘플 수
    # =========================================================
    def __len__(self):
        return len(self.data)

    # =========================================================
    # 9-2. 단일 샘플 반환
    #    Args:
    #        idx (int): 샘플 인덱스
    #
    #    Returns:
    #        item (Dict): 학습에 사용할 텐서 및 메타데이터
    #
    #    Raises:
    #        ValueError: attention_mask 길이가 input_ids와 다릅니다.
    #                    token_type_ids 길이가 input_ids와 다릅니다.
    # 
    # =========================================================
    def __getitem__(self, idx):
        sample = self.data[idx]

        text = sample["text"]
        input_ids = sample["input_ids"]
        attention_mask = sample["attention_mask"]
        token_type_ids = sample.get("token_type_ids", None)
        entities = sample.get("entities", [])

        seq_len = len(input_ids)

        if len(attention_mask) != seq_len:
            raise ValueError(
                f"[{self.split_name}] attention_mask 길이가 input_ids와 다릅니다. "
                f"idx={idx}, len(input_ids)={seq_len}, len(attention_mask)={len(attention_mask)}"
            )

        if token_type_ids is not None and len(token_type_ids) != seq_len:
            raise ValueError(
                f"[{self.split_name}] token_type_ids 길이가 input_ids와 다릅니다. "
                f"idx={idx}, len(input_ids)={seq_len}, len(token_type_ids)={len(token_type_ids)}"
            )

        labels = torch.zeros((self.ent_type_size, seq_len, seq_len), dtype=torch.float32)

        for ent in entities:
            start = ent["start"]
            end = ent["end"]
            label = ent["label"]

            if label not in self.label2id:
                continue
            if not (0 <= start < seq_len and 0 <= end < seq_len):
                continue
            if end < start:
                continue

            labels[self.label2id[label], start, end] = 1.0

        item = {
            "text": text,
            "input_ids": torch.tensor(input_ids, dtype=torch.long),
            "attention_mask": torch.tensor(attention_mask, dtype=torch.long),
            "labels": labels,
            "entities": entities,
            "split": self.split_name,
        }

        if token_type_ids is not None:
            item["token_type_ids"] = torch.tensor(token_type_ids, dtype=torch.long)
        else:
            item["token_type_ids"] = None

        return item


# =========================================================
# 10. Collate 함수
#    Args:
#        features (List[Dict]): Dataset에서 꺼낸 샘플 리스트
#
#    Returns:
#        batch (Dict): 배치 단위 텐서 묶음
# =========================================================
def collate_fn(features):
    batch = {
        "texts": [f["text"] for f in features],
        "input_ids": torch.stack([f["input_ids"] for f in features], dim=0),
        "attention_mask": torch.stack([f["attention_mask"] for f in features], dim=0),
        "labels": torch.stack([f["labels"] for f in features], dim=0),
        "entities": [f["entities"] for f in features],
        "splits": [f["split"] for f in features],
    }

    token_type_ids_list = [f["token_type_ids"] for f in features]
    if all(x is not None for x in token_type_ids_list):
        batch["token_type_ids"] = torch.stack(token_type_ids_list, dim=0)
    else:
        batch["token_type_ids"] = None

    return batch


# =========================================================
# 11. gold entity 추출
#    Args:
#        labels (torch.Tensor): 정답 label tensor, shape = (C, L, L)
#        id2label (Dict[int, str]): id -> label 매핑
#
#    Returns:
#        results (Set[Tuple[str, int, int]]): 정답 엔티티 집합
# =========================================================
def extract_gold_entities_fast(labels: torch.Tensor, id2label: Dict[int, str]) -> Set[Tuple[str, int, int]]:
    indices = torch.nonzero(labels > 0, as_tuple=False)
    return {(id2label[int(c)], int(s), int(e)) for c, s, e in indices}

# =========================================================
# 12. prediction entity 추출
#    Args:
#        logits (torch.Tensor): 예측 logits, shape = (C, L, L)
#        id2label (Dict[int, str]): id -> label 매핑
#        threshold (float): score threshold
#
#    Returns:
#        results (Set[Tuple[str, int, int]]): 예측 엔티티 집합
# =========================================================
def extract_pred_entities_fast(logits: torch.Tensor, id2label: Dict[int, str], threshold: float = 0.0) -> Set[Tuple[str, int, int]]:
    indices = torch.nonzero(logits > threshold, as_tuple=False)
    return {(id2label[int(c)], int(s), int(e)) for c, s, e in indices}

# =========================================================
# 13. F1 계산
#    Args:
#        model: 학습된 모델
#        dataloader: 검증 데이터 로더
#        device (str): 실행 장치
#        id2label (Dict[int, str]): id -> label 매핑
#        threshold (float): score threshold
#        use_amp (bool): AMP 사용 여부
#
#    Returns:
#        precision (float): 정밀도
#        recall (float): 재현율
#        f1 (float): F1 score
# =========================================================
@torch.no_grad()
def compute_f1_fast(model, dataloader, device, id2label, threshold=0.0, use_amp=True):
    model.eval()
    tp, fp, fn = 0, 0, 0

    amp_dtype = torch.float16 if device.startswith("cuda") else torch.float32

    for batch in dataloader:
        input_ids = batch["input_ids"].to(device, non_blocking=True)
        attention_mask = batch["attention_mask"].to(device, non_blocking=True)
        labels = batch["labels"].to(device, non_blocking=True)

        token_type_ids = batch["token_type_ids"]
        if token_type_ids is not None:
            token_type_ids = token_type_ids.to(device, non_blocking=True)

        with torch.autocast(device_type="cuda", dtype=amp_dtype, enabled=(use_amp and device.startswith("cuda"))):
            outputs = model(
                input_ids=input_ids,
                attention_mask=attention_mask,
                token_type_ids=token_type_ids
            )
            logits = outputs["logits"]

        logits = logits.float().cpu()
        labels = labels.cpu()

        for b in range(input_ids.size(0)):
            gold = extract_gold_entities_fast(labels[b], id2label)
            pred = extract_pred_entities_fast(logits[b], id2label, threshold)

            tp += len(gold & pred)
            fp += len(pred - gold)
            fn += len(gold - pred)

    precision = tp / (tp + fp + 1e-12)
    recall = tp / (tp + fn + 1e-12)
    f1 = 2 * precision * recall / (precision + recall + 1e-12)
    return precision, recall, f1


# =========================================================
# 14. logits -> raw entity decode
#    Args:
#        logits (torch.Tensor): 예측 logits, shape = (C, L, L)
#        id2label (Dict[int, str]): id -> label 매핑
#        threshold (float): score threshold
#
#    Returns:
#        entities (List[Dict]): raw prediction 리스트
# =========================================================
def decode_entities_from_logits_fast(logits: torch.Tensor, id2label: Dict[int, str], threshold: float = 0.0):
    """
    logits: (C, L, L)
    """
    indices = torch.nonzero(logits > threshold, as_tuple=False)
    if indices.numel() == 0:
        return []

    scores = logits[indices[:, 0], indices[:, 1], indices[:, 2]]

    entities = []
    for idx, score in zip(indices, scores):
        c, s, e = idx.tolist()
        entities.append({
            "label": id2label[c],
            "start": s,
            "end": e,
            "score": round(float(score.item()), 4)
        })

    entities.sort(key=lambda x: x["score"], reverse=True)
    return entities


