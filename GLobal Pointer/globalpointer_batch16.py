# 15-1. 예측 엔티티를 BIO 태그로 변환
#    Args:
#        seq_len (int): 시퀀스 길이
#        entities (List[Dict]): 예측 엔티티 리스트
#            예: [{"label": "PER", "start": 1, "end": 3, "score": 0.95}, ...]
#
#    Returns:
#        bio_tags (List[str]): BIO 형식의 토큰 단위 태그 리스트
#
#    Notes:
#        - score가 높은 span부터 우선 반영합니다.
#        - 이미 다른 엔티티가 점유한 구간은 중복 반영하지 않습니다.
#        - 범위를 벗어나거나 end < start 인 span은 무시합니다.
# =========================================================
def convert_entities_to_bio(seq_len: int, entities: List[Dict]) -> List[str]:
    """
    span entity -> BIO tag
    entities: [{"label": "PER", "start": 1, "end": 3, "score": 0.95}, ...]
    """
    bio_tags = ["O"] * seq_len

    # score 높은 span부터 우선 반영
    sorted_entities = sorted(entities, key=lambda x: x.get("score", 0.0), reverse=True)
    occupied = [False] * seq_len

    for ent in sorted_entities:
        start = ent["start"]
        end = ent["end"]
        label = ent["label"]

        if not (0 <= start < seq_len and 0 <= end < seq_len):
            continue
        if end < start:
            continue

        # 이미 다른 엔티티가 차지한 구간이면 skip
        if any(occupied[i] for i in range(start, end + 1)):
            continue

        bio_tags[start] = f"B-{label}"
        occupied[start] = True

        for i in range(start + 1, end + 1):
            bio_tags[i] = f"I-{label}"
            occupied[i] = True

    return bio_tags

# =========================================================
# 15-2. 정답 엔티티를 BIO 태그로 변환
#    Args:
#        seq_len (int): 시퀀스 길이
#        gold_entities (List[Dict]): 정답 엔티티 리스트
#            예: [{"label": "PER", "start": 1, "end": 3}, ...]
#
#    Returns:
#        bio_tags (List[str]): 정답 기준 BIO 형식 태그 리스트
#
#    Notes:
#        - gold entity는 일반적으로 score가 없으므로 별도로 처리합니다.
#        - 범위를 벗어나거나 end < start 인 span은 무시합니다.
# =========================================================
def convert_gold_entities_to_bio(seq_len: int, gold_entities: List[Dict]) -> List[str]:
    """
    gold entity -> BIO tag
    gold entity는 score가 없을 수 있으므로 별도 처리
    """
    bio_tags = ["O"] * seq_len

    for ent in gold_entities:
        start = ent["start"]
        end = ent["end"]
        label = ent["label"]

        if not (0 <= start < seq_len and 0 <= end < seq_len):
            continue
        if end < start:
            continue

        bio_tags[start] = f"B-{label}"
        for i in range(start + 1, end + 1):
            bio_tags[i] = f"I-{label}"

    return bio_tags


# =========================================================
# 16. JSON 출력 구성
#    Args:
#        sample (Dict): 원본 샘플 데이터
#            예: {
#                "text": ...,
#                "input_ids": [...],
#                "entities": [...]
#            }
#        pred_entities (List[Dict]): 모델이 예측한 엔티티 리스트
#
#    Returns:
#        output (Dict): JSON 저장용 결과 딕셔너리
#
#    Notes:
#        - tokens는 입력의 토큰 단위를 그대로 유지하기 위해 input_ids를 사용합니다.
#        - gold_bio_tags는 정답 엔티티 기준 BIO 태그입니다.
#        - pred_bio_tags는 예측 엔티티 기준 BIO 태그입니다.
#        - confidence_scores는 raw prediction에서 label/start/end/score만 추출한 정보입니다.
# =========================================================
def build_prediction_output(sample: Dict, pred_entities: List[Dict]) -> Dict:
    seq_len = len(sample["input_ids"])

    pred_bio_tags = convert_entities_to_bio(seq_len, pred_entities)
    gold_bio_tags = convert_gold_entities_to_bio(seq_len, sample.get("entities", []))

    return {
        "text": sample["text"],
        "tokens": sample["input_ids"],  # 토큰 단위 동일하게 유지
        "gold_entities": sample.get("entities", []),
        "gold_bio_tags": gold_bio_tags,
        "raw_predictions": pred_entities,
        "pred_bio_tags": pred_bio_tags,
        "confidence_scores": [
            {
                "label": ent["label"],
                "start": ent["start"],
                "end": ent["end"],
                "score": ent["score"]
            }
            for ent in pred_entities
        ]
    }


