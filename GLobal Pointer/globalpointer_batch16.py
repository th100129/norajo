# 17. DataLoader 생성
#    Args:
#        cfg (Config): 전체 설정 객체
#
#    Returns:
#        label2id (Dict[str, int]): 라벨 -> id 매핑
#        id2label (Dict[int, str]): id -> 라벨 매핑
#        train_loader (DataLoader): 학습 데이터로더
#        valid_loader (DataLoader): 검증 데이터로더
# =========================================================
def build_dataloaders(cfg: Config):
    train_data = load_jsonl_file(cfg.train_json_path)
    valid_data = load_jsonl_file(cfg.valid_json_path)

    print(f"[INFO] train samples: {len(train_data)}")
    print(f"[INFO] valid samples: {len(valid_data)}")

    label2id, id2label = build_label_maps(train_data, valid_data)
    print(f"[INFO] label2id: {label2id}")
    # DT-날짜-0,LC-장소-1,OG-기관-2,PS-사람-3,QT-수량-4,TI-시간-5

    train_dataset = LocalJsonlNERDataset(
        data=train_data,
        label2id=label2id,
        split_name="train"
    )
    valid_dataset = LocalJsonlNERDataset(
        data=valid_data,
        label2id=label2id,
        split_name="valid"
    )

    common_loader_kwargs = {
        "batch_size": cfg.batch_size,
        "collate_fn": collate_fn,
        "num_workers": cfg.num_workers,
        "pin_memory": torch.cuda.is_available(),
        "persistent_workers": cfg.num_workers > 0,
    }

    train_loader = DataLoader(
        train_dataset,
        shuffle=True,
        **common_loader_kwargs
    )
    valid_loader = DataLoader(
        valid_dataset,
        shuffle=False,
        **common_loader_kwargs
    )

    return label2id, id2label, train_loader, valid_loader


# =========================================================
# 18. 학습 함수
#    Args:
#        없음
#
#    Returns:
#        model: 학습 완료된 모델
#        cfg (Config): 설정 객체
#        id2label (Dict[int, str]): id -> label 매핑
# =========================================================
def train():
    cfg = Config()

    label2id, id2label, train_loader, valid_loader = build_dataloaders(cfg)

    model = BertGlobalPointerForNER(
        pretrained_model_name=cfg.pretrained_model_name,
        ent_type_size=len(label2id),
        inner_dim=cfg.inner_dim,
        max_seq_len=512
    ).to(cfg.device)

    optimizer = AdamW(model.parameters(), lr=cfg.lr)
    scaler = torch.cuda.amp.GradScaler(enabled=(cfg.use_amp and cfg.device.startswith("cuda")))

    amp_dtype = torch.float16 if cfg.device.startswith("cuda") else torch.float32

    for epoch in range(cfg.epochs):
        model.train()
        total_loss = 0.0

        for step, batch in enumerate(train_loader):
            input_ids = batch["input_ids"].to(cfg.device, non_blocking=True)
            attention_mask = batch["attention_mask"].to(cfg.device, non_blocking=True)
            labels = batch["labels"].to(cfg.device, non_blocking=True)

            token_type_ids = batch["token_type_ids"]
            if token_type_ids is not None:
                token_type_ids = token_type_ids.to(cfg.device, non_blocking=True)

            optimizer.zero_grad(set_to_none=True)

            with torch.autocast(
                device_type="cuda",
                dtype=amp_dtype,
                enabled=(cfg.use_amp and cfg.device.startswith("cuda"))
            ):
                outputs = model(
                    input_ids=input_ids,
                    attention_mask=attention_mask,
                    token_type_ids=token_type_ids,
                    labels=labels
                )
                loss = outputs["loss"]

            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), cfg.max_grad_norm)
            scaler.step(optimizer)
            scaler.update()

            total_loss += loss.item()

        avg_train_loss = total_loss / max(len(train_loader), 1)

        val_precision, val_recall, val_f1 = compute_f1_fast(
            model=model,
            dataloader=valid_loader,
            device=cfg.device,
            id2label=id2label,
            threshold=cfg.threshold,
            use_amp=cfg.use_amp
        )

        print(
            f"[Epoch {epoch+1}/{cfg.epochs}] "
            f"train_loss={avg_train_loss:.4f} "
            f"valid_precision={val_precision:.4f} "
            f"valid_recall={val_recall:.4f} "
            f"valid_f1={val_f1:.4f}"
        )

    return model, cfg, id2label


