def span_to_bio(text_length, spans):
    bio = ["O"] * text_length

    for span in spans:
        start, end, label = span["start"], span["end"], span["label"]

        if start >= text_length:
            continue

        bio[start] = f"B-{label}"

        # GLiNER의 span end를 포함하는 방식
        for i in range(start + 1, min(end + 1, text_length)):
            bio[i] = f"I-{label}"

    return bio

def token_preds_to_char_bio(offsets, pred_ids, text_length):
    pred_bio = ["O"] * text_length

    usable_len = min(len(offsets), len(pred_ids))
    for idx in range(usable_len):
        start, end = offsets[idx]
        if start == end:
            continue

        tag = id2label.get(pred_ids[idx], "O")
        if tag == "O":
            continue

        if start < text_length:
            pred_bio[start] = tag

        inside_tag = tag.replace("B-", "I-")
        for char_idx in range(start + 1, min(end, text_length)):
            pred_bio[char_idx] = inside_tag

    return pred_bio

def globalpointer_to_bio(scores, offsets, text_length, threshold=0.0):
    pred_bio = ["O"] * text_length

    candidates = []
    for label_id, start_tok, end_tok in zip(*np.where(scores > threshold)):
        if label_id >= len(ENTITY_TYPES):
            continue
        if start_tok >= len(offsets) or end_tok >= len(offsets):
            continue

        char_start = offsets[start_tok][0]
        char_end = offsets[end_tok][1]

        if char_start == char_end:
            continue
        if char_start >= text_length:
            continue

        score = scores[label_id, start_tok, end_tok]
        candidates.append((score, label_id, char_start, char_end))

    # 점수 높은 순으로 정렬
    candidates.sort(key=lambda x: x[0], reverse=True)

    occupied = [False] * text_length

    for score, label_id, char_start, char_end in candidates:
        # 이미 다른 span이 차지한 구간이면 스킵
        overlap = False
        for i in range(char_start, min(char_end, text_length)):
            if occupied[i]:
                overlap = True
                break
        if overlap:
            continue

        label = ENTITY_TYPES[label_id]
        pred_bio[char_start] = f"B-{label}"
        for i in range(char_start + 1, min(char_end, text_length)):
            pred_bio[i] = f"I-{label}"

        for i in range(char_start, min(char_end, text_length)):
            occupied[i] = True

    return pred_bio

