for data in tqdm(dataset):
    tokens = data["tokens"]
    raw_true_bio = data["bio_tags"]

    # 15개 valid 라벨 중 6개 엔티티만 남김
    true_bio = filter_bio_tags_to_target_entities(raw_true_bio, ENTITY_TYPES)

    text = "".join(tokens)
    text_length = len(tokens)

    all_true_bio.append(true_bio)

    # -----------------------------
    # 공통 입력 (CRF / GP)
    # -----------------------------
    inputs = tokenizer(
        text,
        return_tensors="pt",
        max_length=128,
        truncation=True,
        return_offsets_mapping=True
    )

    offsets = inputs["offset_mapping"][0].cpu().numpy()

    model_inputs = {
        "input_ids": inputs["input_ids"].to(device),
        "attention_mask": inputs["attention_mask"].to(device)
    }

    # -----------------------------
    # 1) CRF
    # -----------------------------
    with torch.no_grad():
        _, predictions = model_crf(
            input_ids=model_inputs["input_ids"],
            attention_mask=model_inputs["attention_mask"]
        )

    pred_ids_crf = predictions[0]
    pred_bio_crf = token_preds_to_char_bio(offsets, pred_ids_crf, text_length)
    all_pred_crf.append(pred_bio_crf)

    # -----------------------------
    # 2) GLiNER
    # -----------------------------
    features = build_inference_features(tokens, tokenizer, ENTITY_TYPES)

    with torch.no_grad():
        pred_spans_gliner = model_gliner.predict(
            input_ids=features["input_ids"].to(device),
            attention_mask=features["attention_mask"].to(device),
            text_mask=features["text_mask"].to(device),
            ent_mask=features["ent_mask"].to(device),
            word_index=features["word_index"].to(device)
        )

    pred_bio_gliner = span_to_bio(text_length, pred_spans_gliner[0])
    all_pred_gliner.append(pred_bio_gliner)

    # -----------------------------
    # 3) GlobalPointer
    # -----------------------------
    with torch.no_grad():
        logits = model_gp(
            model_inputs["input_ids"],
            model_inputs["attention_mask"]
        )

    scores = logits[0].detach().cpu().numpy()
    pred_bio_gp = globalpointer_to_bio(scores, offsets, text_length, threshold=0.0)
    all_pred_gp.append(pred_bio_gp)

