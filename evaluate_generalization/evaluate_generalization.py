def print_detailed_metrics(true_bio, pred_bio, model_name, icon):
    p = precision_score(true_bio, pred_bio)
    r = recall_score(true_bio, pred_bio)
    f = f1_score(true_bio, pred_bio)

    tp, fp, fn = 0, 0, 0
    for t_seq, p_seq in zip(true_bio, pred_bio):
        t_ents = set(get_entities(t_seq))
        p_ents = set(get_entities(p_seq))

        tp += len(t_ents & p_ents)
        fp += len(p_ents - t_ents)
        fn += len(t_ents - p_ents)

    print(f"{icon} [{model_name}]")
    print(f"   Precision : {p:.4f}")
    print(f"   Recall    : {r:.4f}")
    print(f"   F1        : {f:.4f}")
    print(f"   TP={tp}, FP={fp}, FN={fn}")
    print("-" * 60)

