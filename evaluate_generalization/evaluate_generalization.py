class SinusoidalPositionEmbedding(nn.Module):
    def __init__(self, output_dim, merge_mode="zero"):
        super().__init__()
        self.output_dim = output_dim
        self.merge_mode = merge_mode

    def forward(self, inputs):
        batch_size, seq_len = inputs.shape[:2]
        device = inputs.device

        position_ids = torch.arange(0, seq_len, dtype=torch.float, device=device).unsqueeze(-1)
        indices = torch.arange(0, self.output_dim // 2, dtype=torch.float, device=device)
        indices = torch.pow(10000.0, -2 * indices / self.output_dim)

        embeddings = position_ids * indices
        embeddings = torch.stack([torch.sin(embeddings), torch.cos(embeddings)], dim=-1)
        embeddings = embeddings.reshape(seq_len, self.output_dim)
        embeddings = embeddings.unsqueeze(0).repeat(batch_size, 1, 1)

        if self.merge_mode == "zero":
            return embeddings
        elif self.merge_mode == "add":
            return inputs + embeddings
        else:
            raise ValueError("merge_mode must be 'zero' or 'add'")

def apply_rope(x, pos):
    cos_pos = pos[..., 1::2].repeat_interleave(2, dim=-1).unsqueeze(2)
    sin_pos = pos[..., ::2].repeat_interleave(2, dim=-1).unsqueeze(2)

    x2 = torch.stack([-x[..., 1::2], x[..., ::2]], dim=-1)
    x2 = x2.reshape_as(x)

    return x * cos_pos + x2 * sin_pos

class GlobalPointer(nn.Module):
    def __init__(self, encoder_name: str, ent_type_size: int, inner_dim: int = 64):
        super().__init__()
        self.encoder = AutoModel.from_pretrained(encoder_name)
        self.ent_type_size = ent_type_size
        self.inner_dim = inner_dim
        self.hidden_size = self.encoder.config.hidden_size

        self.dense = nn.Linear(self.hidden_size, ent_type_size * inner_dim * 2)
        self.position_embedding = SinusoidalPositionEmbedding(inner_dim, merge_mode="zero")

    def forward(self, input_ids, attention_mask):
        outputs = self.encoder(input_ids=input_ids, attention_mask=attention_mask)
        last_hidden_state = outputs.last_hidden_state

        logits = self.dense(last_hidden_state)
        logits = torch.split(logits, self.inner_dim * 2, dim=-1)
        logits = torch.stack(logits, dim=-2)

        qw, kw = logits[..., :self.inner_dim], logits[..., self.inner_dim:]

        pos = self.position_embedding(last_hidden_state)
        qw = apply_rope(qw, pos)
        kw = apply_rope(kw, pos)

        logits = torch.einsum("bmhd,bnhd->bhmn", qw, kw)

        pad_mask = attention_mask.unsqueeze(1).unsqueeze(1) * attention_mask.unsqueeze(1).unsqueeze(2)
        logits = logits * pad_mask - (1 - pad_mask) * 1e12

        lower_mask = torch.tril(torch.ones_like(logits), diagonal=-1)
        logits = logits - lower_mask * 1e12

        return logits / (self.inner_dim ** 0.5)

