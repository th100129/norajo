class BertCRF(nn.Module):
    def __init__(self, model_name, num_classes, dropout=0.1):
        super().__init__()
        self.bert = AutoModel.from_pretrained(model_name)
        hidden_size = self.bert.config.hidden_size
        self.dropout = nn.Dropout(dropout)
        self.position_wise_ff = nn.Linear(hidden_size, num_classes)
        self.crf = CRF(num_tags=num_classes, batch_first=True)

    def forward(self, input_ids, attention_mask=None, tags=None):
        outputs = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        sequence_output = self.dropout(outputs.last_hidden_state)
        emissions = self.position_wise_ff(sequence_output)

        crf_mask = attention_mask.bool() if attention_mask is not None else torch.ones_like(input_ids, dtype=torch.bool)

        if tags is not None:
            log_likelihood = self.crf(emissions, tags, mask=crf_mask, reduction="mean")
            pred = self.crf.decode(emissions, mask=crf_mask)
            return -log_likelihood, pred
        else:
            pred = self.crf.decode(emissions, mask=crf_mask)
            return None, pred

